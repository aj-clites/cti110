# A program that will take an integer as an input and produce an output with the square and cube of the integer.

int1 = int(input('Enter integer:\n'))

print('You entered:', int1)

intSquared = int1 ** 2

intCubed = int1 ** 3

print(int1, 'squared is', intSquared)
print('And', int1, 'cubed is', intCubed)

int2 = int(input('Enter another integer:\n'))

sum1 = int1 + int2

product = int1 * int2

print(int1, '+', int2, 'is', sum1)
print(int1, '*', int2, 'is', product)
